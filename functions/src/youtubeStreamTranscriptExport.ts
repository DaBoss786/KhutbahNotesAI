/**
 * Sleep for the provided number of milliseconds.
 *
 * @param {number} delayMs Delay in milliseconds.
 * @return {Promise<void>} Resolves after the delay.
 */
function delay(delayMs: number): Promise<void> {
  return new Promise((resolve) => setTimeout(resolve, delayMs));
}

/**
 * Fetch a URL as text with a browser-like user agent.
 *
 * @param {string} url URL to fetch.
 * @return {Promise<string>} Response body text.
 */
async function fetchText(url: string): Promise<string> {
  try {
    return await fetchTextViaCurl(url);
  } catch {
    const response = await fetch(url, {
      method: "GET",
      redirect: "follow",
      headers: DEFAULT_HEADERS,
    });
    if (!response.ok) {
      throw new Error(`HTTP ${response.status} for ${url}`);
    }
    return response.text();
  }
}

/**
 * Fetch a URL as text using curl for better compatibility with YouTube pages.
 *
 * @param {string} url URL to fetch.
 * @return {Promise<string>} Response body text.
 */
function fetchTextViaCurl(url: string): Promise<string> {
  return new Promise((resolve, reject) => {
    execFile(
      "curl",
      [
        "-L",
        "--silent",
        "--show-error",
        "--max-time",
        "30",
        "-A",
        DEFAULT_HEADERS["User-Agent"],
        "-H",
        `Accept-Language: ${DEFAULT_HEADERS["Accept-Language"]}`,
        url,
      ],
      {maxBuffer: 10 * 1024 * 1024},
      (error, stdout) => {
        if (error) {
          reject(error);
          return;
        }
        resolve(stdout);
      }
    );
  });
}

/**
 * Return a plain object view when possible.
 *
 * @param {unknown} value Candidate value.
 * @return {Record<string, unknown> | null} Plain record or null.
 */
function asRecord(value: unknown): Record<string, unknown> | null {
  return value && typeof value === "object" && !Array.isArray(value) ?
    value as Record<string, unknown> :
    null;
}

/**
 * Read a nested value from an object path.
 *
 * @param {unknown} value Root value.
 * @param {Array<string | number>} keys Object path.
 * @return {unknown} Nested value.
 */
function getNested(
  value: unknown,
  keys: Array<string | number>
): unknown {
  let current: unknown = value;
  for (const key of keys) {
    if (typeof key === "number") {
      if (!Array.isArray(current) || key >= current.length) {
        return undefined;
      }
      current = current[key];
      continue;
    }

    const record = asRecord(current);
    if (!record || !(key in record)) {
      return undefined;
    }
    current = record[key];
  }
  return current;
}

/**
 * Flatten text content from common YouTube text objects.
 *
 * @param {unknown} value Raw YouTube text payload.
 * @return {string} Flattened text.
 */
function flattenText(value: unknown): string {
  if (typeof value === "string") {
    return value.trim();
  }

  const record = asRecord(value);
  if (!record) {
    return "";
  }

  if (typeof record.content === "string") {
    return record.content.trim();
  }

  const simpleText = record.simpleText;
  if (typeof simpleText === "string") {
    return simpleText.trim();
  }

  const runs = record.runs;
  if (Array.isArray(runs)) {
    return runs
      .map((run) => {
        const runRecord = asRecord(run);
        return runRecord && typeof runRecord.text === "string" ?
          runRecord.text :
          "";
      })
      .join("")
      .trim();
  }

  return "";
}

/**
 * Extract a JSON object that appears after a marker string.
 *
 * @param {string} source Full source text.
 * @param {string} marker Marker preceding the JSON object.
 * @return {unknown | null} Parsed JSON object or null.
 */
function extractJsonObjectAfterMarker(
  source: string,
  marker: string
): unknown | null {
  const markerIndex = source.indexOf(marker);
  if (markerIndex < 0) {
    return null;
  }

  const objectStart = source.indexOf("{", markerIndex + marker.length);
  if (objectStart < 0) {
    return null;
  }

  let depth = 0;
  let inString = false;
  let escaped = false;
  for (let i = objectStart; i < source.length; i++) {
    const char = source[i];
    if (inString) {
      if (escaped) {
        escaped = false;
      } else if (char === "\\") {
        escaped = true;
      } else if (char === "\"") {
        inString = false;
      }
      continue;
    }

    if (char === "\"") {
      inString = true;
      continue;
    }
    if (char === "{") {
      depth++;
      continue;
    }
    if (char === "}") {
      depth--;
      if (depth === 0) {
        const rawJson = source.slice(objectStart, i + 1);
        try {
          return JSON.parse(rawJson);
        } catch {
          return null;
        }
      }
    }
  }

  return null;
}

/**
 * Parse YouTube initial data from raw channel HTML.
 *
 * @param {string} html Channel page HTML.
 * @return {Record<string, unknown>} Parsed initial data.
 */
function extractInitialDataFromHtml(
  html: string
): Record<string, unknown> {
  const markers = [
    "var ytInitialData = ",
    "window['ytInitialData'] = ",
    "window[\"ytInitialData\"] = ",
    "ytInitialData = ",
  ];
  for (const marker of markers) {
    const parsed = extractJsonObjectAfterMarker(html, marker);
    const record = asRecord(parsed);
    if (record) {
      return record;
    }
  }

  throw new Error("Unable to parse YouTube initial data from channel page.");
}

/**
 * Pick the Streams tab from YouTube initial data when available.
 *
 * @param {Record<string, unknown>} initialData Parsed initial data.
 * @return {Record<string, unknown> | null} Tab renderer payload or null.
 */
function findStreamsTab(
  initialData: Record<string, unknown>
): Record<string, unknown> | null {
  const tabs = getNested(
    initialData,
    ["contents", "twoColumnBrowseResultsRenderer", "tabs"]
  );
  if (!Array.isArray(tabs)) {
    return null;
  }

  for (const tab of tabs) {
    const renderer = asRecord(asRecord(tab)?.tabRenderer);
    if (!renderer) {
      continue;
    }

    const title = flattenText(renderer.title).toLowerCase();
    const endpointUrl = flattenText(
      getNested(
        renderer,
        ["endpoint", "commandMetadata", "webCommandMetadata", "url"]
      )
    ).toLowerCase();
    if (title === "streams" || endpointUrl.endsWith("/streams")) {
      return renderer;
    }
  }

  return null;
}

/**
 * Extract a rich-grid contents array from a tab renderer.
 *
 * @param {Record<string, unknown> | null} tabRenderer Tab renderer payload.
 * @return {unknown[] | null} Grid contents or null.
 */
function extractRichGridContents(
  tabRenderer: Record<string, unknown> | null
): unknown[] | null {
  const contents = getNested(
    tabRenderer,
    ["content", "richGridRenderer", "contents"]
  );
  return Array.isArray(contents) ? contents : null;
}

/**
 * Pick a fallback channel tab that exposes a video grid.
 *
 * Used for channels that do not expose a dedicated Streams tab.
 *
 * @param {Record<string, unknown>} initialData Parsed initial data.
 * @return {Record<string, unknown> | null} Tab renderer payload or null.
 */
function findFallbackChannelTab(
  initialData: Record<string, unknown>
): Record<string, unknown> | null {
  const tabs = getNested(
    initialData,
    ["contents", "twoColumnBrowseResultsRenderer", "tabs"]
  );
  if (!Array.isArray(tabs)) {
    return null;
  }

  for (const tab of tabs) {
    const renderer = asRecord(asRecord(tab)?.tabRenderer);
    if (!renderer) {
      continue;
    }

    const title = flattenText(renderer.title).toLowerCase();
    if (!["home", "videos", "live"].includes(title)) {
      continue;
    }
    if (extractRichGridContents(renderer)) {
      return renderer;
    }
  }

  for (const tab of tabs) {
    const renderer = asRecord(asRecord(tab)?.tabRenderer);
    if (renderer && extractRichGridContents(renderer)) {
      return renderer;
    }
  }

  return null;
}

/**
 * Convert a streams tab item into a normalized stream video payload.
 *
 * @param {unknown} rawItem Rich grid item payload.
 * @return {StreamVideo | null} Stream video payload or null.
 */
function normalizeStreamVideo(rawItem: unknown): StreamVideo | null {
  const richItem = asRecord(asRecord(rawItem)?.richItemRenderer);
  const content = asRecord(richItem?.content);
  const videoRenderer = asRecord(content?.videoRenderer);
  if (videoRenderer) {
    const videoId = flattenText(videoRenderer.videoId);
    const title = flattenText(videoRenderer.title);
    const relativeUrl = flattenText(
      getNested(
        videoRenderer,
        ["navigationEndpoint", "commandMetadata", "webCommandMetadata", "url"]
      )
    );
    if (!videoId || !title || !relativeUrl.startsWith("/watch")) {
      return null;
    }

    return {
      videoId,
      title,
      videoUrl: `https://www.youtube.com${relativeUrl}`,
      publishedText: flattenText(videoRenderer.publishedTimeText),
    };
  }

  const lockup = asRecord(content?.lockupViewModel);
  if (!lockup) {
    return null;
  }

  const videoId = flattenText(lockup.contentId);
  const title = flattenText(
    getNested(lockup, ["metadata", "lockupMetadataViewModel", "title"])
  );
  const relativeUrl = flattenText(
    getNested(
      lockup,
      [
        "rendererContext",
        "commandContext",
        "onTap",
        "innertubeCommand",
        "commandMetadata",
        "webCommandMetadata",
        "url",
      ]
    )
  );
  if (!videoId || !title || !relativeUrl.startsWith("/watch")) {
    return null;
  }

  const metadataRows = getNested(
    lockup,
    [
      "metadata",
      "lockupMetadataViewModel",
      "metadata",
      "contentMetadataViewModel",
      "metadataRows",
    ]
  );
  const metadataParts: string[] = [];
  if (Array.isArray(metadataRows)) {
    for (const row of metadataRows) {
      const parts = getNested(row, ["metadataParts"]);
      if (!Array.isArray(parts)) {
        continue;
      }
      for (const part of parts) {
        const text = flattenText(getNested(part, ["text"]));
        if (text) {
          metadataParts.push(text);
        }
      }
    }
  }

  const publishedText =
    metadataParts.find((part) => part.toLowerCase().includes("streamed")) ??
    metadataParts.join(" | ");

  return {
    videoId,
    title,
    videoUrl: `https://www.youtube.com${relativeUrl}`,
    publishedText,
  };
}

/**
 * Extract the recent stream videos shown on the channel page.
 *
 * @param {string} html Channel page HTML.
 * @param {number} limit Maximum number of videos to return.
 * @return {StreamVideo[]} Recent stream videos in page order.
 */
export function extractRecentStreamVideosFromHtml(
  html: string,
  limit?: number
): StreamVideo[] {
  if (limit !== undefined && (!Number.isInteger(limit) || limit <= 0)) {
    throw new Error("limit must be a positive integer.");
  }

  const initialData = extractInitialDataFromHtml(html);
  const streamsTab = findStreamsTab(initialData);
  const fallbackTab = findFallbackChannelTab(initialData);
  const contents =
    extractRichGridContents(streamsTab) ??
    extractRichGridContents(fallbackTab);
  if (!contents) {
    throw new Error(
      streamsTab && !fallbackTab ?
        "Streams tab is missing rich grid contents." :
        "Could not find a YouTube channel tab with recent videos."
    );
  }

  const videos: StreamVideo[] = [];
  for (const item of contents) {
    const video = normalizeStreamVideo(item);
    if (!video) {
      continue;
    }
    videos.push(video);
    if (limit !== undefined && videos.length >= limit) {
      break;
    }
  }

  if (videos.length === 0) {
    throw new Error("No stream videos were found on the channel page.");
  }

  return videos;
}

/**
 * Discover recent stream videos from a YouTube channel streams page.
 *
 * @param {string} channelUrl Channel streams URL.
 * @param {number} limit Maximum number of videos to return.
 * @param {RawTextFetcher} fetcher Override fetcher for tests.
 * @param {ExportFilters} filters Optional title filters.
 * @return {Promise<StreamVideo[]>} Recent stream videos.
 */
export async function discoverRecentStreamVideos(
  channelUrl: string,
  limit: number,
  fetcher: RawTextFetcher = fetchText,
  filters: ExportFilters = {}
): Promise<StreamVideo[]> {
  let lastError: unknown = null;
  for (let attempt = 1; attempt <= STREAM_DISCOVERY_ATTEMPTS; attempt++) {
    try {
      const html = await fetcher(channelUrl);
      const discovered = extractRecentStreamVideosFromHtml(html);
      const filtered = filterStreamVideos(discovered, filters);
      if (filtered.length === 0) {
        throw new Error(
          "No matching stream videos were found on the channel page."
        );
      }
      return filtered.slice(0, limit);
    } catch (error) {
      lastError = error;
      if (attempt < STREAM_DISCOVERY_ATTEMPTS) {
        await delay(attempt * 750);
      }
    }
  }

  if (lastError instanceof Error) {
    throw lastError;
  }
  throw new Error("Unable to discover recent stream videos.");
}

/**
 * Extract YouTube player response from watch page HTML.
 *
 * @param {string} watchHtml Raw watch page HTML.
 * @return {Record<string, unknown> | null} Parsed player response.
 */
function extractPlayerResponseFromWatchHtml(
  watchHtml: string
): Record<string, unknown> | null {
  const markers = [
    "ytInitialPlayerResponse = ",
    "var ytInitialPlayerResponse = ",
    "window['ytInitialPlayerResponse'] = ",
    "window[\"ytInitialPlayerResponse\"] = ",
  ];
  for (const marker of markers) {
    const parsed = extractJsonObjectAfterMarker(watchHtml, marker);
    const record = asRecord(parsed);
    if (record) {
      return record;
    }
  }
  return null;
}

/**
 * Parse the first valid date candidate.
 *
 * @param {unknown[]} values Candidate raw values.
 * @return {Date | null} Parsed date or null.
 */
function parseFirstValidDate(values: unknown[]): Date | null {
  for (const value of values) {
    if (typeof value !== "string") {
      continue;
    }
    const trimmed = value.trim();
    if (!trimmed) {
      continue;
    }
    const parsed = new Date(trimmed);
    if (!Number.isNaN(parsed.getTime())) {
      return parsed;
    }
  }
  return null;
}

/**
 * Infer a speaker name from a YouTube title when possible.
 *
 * @param {string} title Video title.
 * @return {string | null} Parsed speaker or null.
 */
export function inferSpeakerFromTitle(title: string): string | null {
  const cleaned = title.replace(/\s+/g, " ").trim();
  if (!cleaned) {
    return null;
  }

  const cleanCandidate = (candidate: string): string | null => {
    const normalized = candidate
      .replace(
        /^(?:khutbah|khutba|jummah|jumu'?ah)\s+by\s+/i,
        ""
      )
      .replace(/^by\s+/i, "")
      .trim();
    if (!normalized || /^\d/.test(normalized)) {
      return null;
    }
    return normalized;
  };

  const byMatch = cleaned.match(
    /(?:khutbah|khutba|jummah|jumu'?ah)[^|:-]*\bby\s+(.+)$/i
  );
  if (byMatch?.[1]) {
    return cleanCandidate(byMatch[1]);
  }

  const honorificPattern =
    /^(?:imam|shaykh(?:\s+dr\.?)?|sheikh|dr\.?|ustadh|mufti)\b/i;
  const segments = cleaned
    .split("|")
    .map((segment) => segment.trim())
    .filter((segment) => segment.length > 0);
  for (let i = segments.length - 1; i >= 0; i--) {
    if (honorificPattern.test(segments[i])) {
      return cleanCandidate(segments[i]);
    }
  }

  const dashMatch = cleaned.match(
    /[-–—]\s*((?:imam|shaykh(?:\s+dr\.?)?|sheikh|dr\.?|ustadh|mufti)\b.+)$/i
  );
  if (dashMatch?.[1]) {
    return cleanCandidate(dashMatch[1]);
  }

  return null;
}

/**
 * Extract publish date and speaker metadata from watch HTML.
 *
 * @param {string} watchHtml Raw watch page HTML.
 * @param {string} title Video title for speaker heuristics.
 * @return {WatchVideoMetadata} Parsed metadata.
 */
export function extractWatchVideoMetadataFromHtml(
  watchHtml: string,
  title: string
): WatchVideoMetadata {
  const playerResponse = extractPlayerResponseFromWatchHtml(watchHtml);
  const publishedAt = parseFirstValidDate([
    getNested(playerResponse, [
      "microformat",
      "playerMicroformatRenderer",
      "publishDate",
    ]),
    getNested(playerResponse, [
      "microformat",
      "playerMicroformatRenderer",
      "uploadDate",
    ]),
    watchHtml.match(/"publishDate":"([^"]+)"/)?.[1],
    watchHtml.match(/"uploadDate":"([^"]+)"/)?.[1],
    watchHtml.match(/itemprop="datePublished"\s+content="([^"]+)"/)?.[1],
    watchHtml.match(/itemprop="uploadDate"\s+content="([^"]+)"/)?.[1],
  ]);

  return {
    publishedAt,
    speaker: inferSpeakerFromTitle(title),
  };
}

