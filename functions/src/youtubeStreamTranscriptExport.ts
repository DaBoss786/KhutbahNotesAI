/**
 * Remove non-ASCII characters after Unicode normalization.
 *
 * @param {string} value Input text.
 * @return {string} ASCII-only text.
 */
function stripNonAscii(value: string): string {
  return Array.from(value)
    .filter((char) => char.charCodeAt(0) <= 0x7f)
    .join("");
}

